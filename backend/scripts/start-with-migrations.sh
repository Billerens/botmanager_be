#!/bin/sh

# –°–∫—Ä–∏–ø—Ç –∑–∞–ø—É—Å–∫–∞ BotManager API —Å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–º –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–µ–º –º–∏–≥—Ä–∞—Ü–∏–π
# –≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –≤ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–µ

set -e  # –í—ã—Ö–æ–¥–∏–º –ø—Ä–∏ –ª—é–±–æ–π –æ—à–∏–±–∫–µ

echo "üöÄ –ó–∞–ø—É—Å–∫ BotManager API..."
echo "üìÖ $(date)"

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
check_database_connection() {
    echo "üîÑ –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö..."
    
    # –ü—ã—Ç–∞–µ–º—Å—è –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö —Å —Ç–∞–π–º–∞—É—Ç–æ–º
    timeout=30
    counter=0
    last_error=""
    
    while [ $counter -lt $timeout ]; do
        connection_output=$(npm run typeorm -- query "SELECT 1" --dataSource dist/database/data-source.js 2>&1)
        connection_exit_code=$?
        
        if [ $connection_exit_code -eq 0 ]; then
            echo "‚úÖ –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –ø–æ–¥–∫–ª—é—á–µ–Ω–∞"
            return 0
        fi
        
        last_error="$connection_output"
        echo "‚è≥ –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∞ - –∂–¥–µ–º... ($counter/$timeout)"
        sleep 2
        counter=$((counter + 2))
    done
    
    echo "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö –∑–∞ $timeout —Å–µ–∫—É–Ω–¥"
    echo ""
    echo "üìã –ü–æ—Å–ª–µ–¥–Ω—è—è –æ—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è:"
    echo "----------------------------------------"
    echo "$last_error"
    echo "----------------------------------------"
    echo ""
    echo "üîß –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ë–î:"
    echo "  - Host: ${DATABASE_HOST:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
    echo "  - Port: ${DATABASE_PORT:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
    echo "  - Database: ${DATABASE_NAME:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
    echo "  - User: ${DATABASE_USERNAME:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
    echo ""
    echo "üí° –í–æ–∑–º–æ–∂–Ω—ã–µ –ø—Ä–∏—á–∏–Ω—ã:"
    echo "  1. –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –µ—â–µ –Ω–µ –∑–∞–ø—É—â–µ–Ω–∞"
    echo "  2. –ù–µ–≤–µ—Ä–Ω—ã–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è"
    echo "  3. –°–µ—Ç–µ–≤—ã–µ –ø—Ä–æ–±–ª–µ–º—ã –∏–ª–∏ firewall"
    echo "  4. –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–µ –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è"
    echo "  5. –ù–µ–≤–µ—Ä–Ω—ã–µ —É—á–µ—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ"
    echo ""
    return 1
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –º–∏–≥—Ä–∞—Ü–∏–π
run_migrations() {
    echo "üîÑ –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å –º–∏–≥—Ä–∞—Ü–∏–π..."
    
    # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–µ–∫—É—â–∏–π —Å—Ç–∞—Ç—É—Å –º–∏–≥—Ä–∞—Ü–∏–π
    migration_show_output=$(npm run typeorm -- migration:show --dataSource dist/database/data-source.js 2>&1) || true
    echo "$migration_show_output"
    
    echo "üîÑ –ü—Ä–∏–º–µ–Ω—è–µ–º –º–∏–≥—Ä–∞—Ü–∏–∏..."
    
    # –ü—Ä–∏–º–µ–Ω—è–µ–º –º–∏–≥—Ä–∞—Ü–∏–∏ —Å –∑–∞—Ö–≤–∞—Ç–æ–º –≤—ã–≤–æ–¥–∞
    migration_output=$(npm run typeorm -- migration:run --dataSource dist/database/data-source.js 2>&1)
    migration_exit_code=$?
    
    if [ $migration_exit_code -eq 0 ]; then
        echo "‚úÖ –ú–∏–≥—Ä–∞—Ü–∏–∏ —É—Å–ø–µ—à–Ω–æ –ø—Ä–∏–º–µ–Ω–µ–Ω—ã"
        echo "$migration_output"
        
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ñ–∏–Ω–∞–ª—å–Ω—ã–π —Å—Ç–∞—Ç—É—Å
        echo "üìä –§–∏–Ω–∞–ª—å–Ω—ã–π —Å—Ç–∞—Ç—É—Å –º–∏–≥—Ä–∞—Ü–∏–π:"
        npm run typeorm -- migration:show --dataSource dist/database/data-source.js || true
    else
        echo "‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–∏ –º–∏–≥—Ä–∞—Ü–∏–π"
        echo "üîç –ö–æ–¥ –æ—à–∏–±–∫–∏: $migration_exit_code"
        echo ""
        echo "üìã –î–µ—Ç–∞–ª–∏ –æ—à–∏–±–∫–∏:"
        echo "----------------------------------------"
        echo "$migration_output"
        echo "----------------------------------------"
        echo ""
        echo "üîß –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ë–î:"
        echo "  - Host: ${DATABASE_HOST:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
        echo "  - Port: ${DATABASE_PORT:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
        echo "  - Database: ${DATABASE_NAME:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
        echo "  - User: ${DATABASE_USERNAME:-–Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω}"
        echo ""
        echo "üí° –í–æ–∑–º–æ–∂–Ω—ã–µ –ø—Ä–∏—á–∏–Ω—ã:"
        echo "  1. –û—à–∏–±–∫–∞ –≤ –∫–æ–¥–µ –º–∏–≥—Ä–∞—Ü–∏–∏"
        echo "  2. –ù–∞—Ä—É—à–µ–Ω–∏–µ —Ü–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç–∏ –¥–∞–Ω–Ω—ã—Ö –∏–ª–∏ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–π –ë–î"
        echo "  3. –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –ø—Ä–∞–≤ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ë–î"
        echo "  4. –ö–æ–Ω—Ñ–ª–∏–∫—Ç —Å —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–º–∏ –¥–∞–Ω–Ω—ã–º–∏"
        echo "  5. –ù–µ—Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç—å —Å—Ö–µ–º—ã –ë–î"
        echo ""
        return 1
    fi
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
start_application() {
    echo "üöÄ –ó–∞–ø—É—Å–∫–∞–µ–º BotManager API..."
    exec npm run start:prod
}

# –û—Å–Ω–æ–≤–Ω–∞—è –ª–æ–≥–∏–∫–∞
main() {
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
    if ! check_database_connection; then
        echo "‚ùå –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞: –Ω–µ —É–¥–∞–ª–æ—Å—å –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö"
        exit 1
    fi
    
    # –ü—Ä–∏–º–µ–Ω—è–µ–º –º–∏–≥—Ä–∞—Ü–∏–∏
    if ! run_migrations; then
        echo "‚ùå –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞: –Ω–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–∏–º–µ–Ω–∏—Ç—å –º–∏–≥—Ä–∞—Ü–∏–∏"
        exit 1
    fi
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ
    start_application
}

# –ó–∞–ø—É—Å–∫–∞–µ–º –æ—Å–Ω–æ–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é
main "$@"
